class UserInfo {
  String userType;
  String yourId;
  String youName;
  String yourPassword;
  String yourPhone;

  String get user_type {
    return userType;
  }

  set user_type(String userType) {
    this.userType = userType;
  }

  String get your_id {
    return yourId;
  }

  set your_id(String yourId) {
    this.yourId = yourId;
  }

  String get your_name {
    return youName;
  }

  set your_name(String youName) {
    this.youName = youName;
  }

  String get your_password {
    return yourPassword;
  }

  set your_password(String yourPassword) {
    this.yourPassword = yourPassword;
  }

  String get your_phone {
    return yourPhone;
  }

  set your_phone(String yourPhone) {
    this.yourPhone = yourPhone;
  }
}
