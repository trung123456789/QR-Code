class MonthTaskInfo {
  String taskId;
  String taskName;
  String technicianName;
  String taskSize;
  String taskDate;

  String get task_id {
    return taskId;
  }

  set task_id(String taskId) {
    this.taskId = taskId;
  }

  String get task_name {
    return taskName;
  }

  set task_name(String taskName) {
    this.taskName = taskName;
  }

  String get technician_name {
    return technicianName;
  }

  set technician_name(String technicianName) {
    this.technicianName = technicianName;
  }

  String get task_size {
    return taskSize;
  }

  set task_size(String taskSize) {
    this.taskSize = taskSize;
  }

  String get task_date {
    return taskDate;
  }

  set task_date(String taskDate) {
    this.taskDate = taskDate;
  }
}
